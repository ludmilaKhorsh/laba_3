package org.example;

public class Main {
    public static void main(String[] args) {
            int[][] matrix = {{-1, -2, -3},
                    {1, 2, 3},
                    {1, 2, 4},
                    {3,-5,6}};
        for (int i=0; i<matrix[i].length; i++) {
            for (int j = 0; j < matrix[j].length; j++)
                System.out.print(matrix[i][j] + " ");
        }
        System.out.println();
    }


    }
